<template>

	<Head title="Log in" />

	<jet-authentication-card>
		<template #logo>
			<!-- <jet-authentication-card-logo /> -->
            <img src="../../../../public/images/logo/new-logo.png" style="width: 150px;" />
		</template>

		<jet-validation-errors class="mb-4" />

		<div v-if="status" class="mb-4 font-medium text-sm text-green-600">
			{{ status }}
		</div>

		<form @submit.prevent="submit">
			<div>
				<jet-label for="email" value="Email" />
				<jet-input id="email" type="email" class="mt-1 block w-full" v-model="form.email" required autofocus />
			</div>

			<div class="mt-4">
				<jet-label for="password" value="Password" />
				<jet-input id="password" type="password" class="mt-1 block w-full" v-model="form.password" required autocomplete="current-password" />
			</div>

			<div class="flex mt-4 justify-between">
				<label class="flex items-center">
					<jet-checkbox name="remember" v-model:checked="form.remember" />
					<span class="ml-2 text-sm text-gray-800">Remember me</span>
				</label>
				<Link v-if="canResetPassword" :href="route('password.request')" class="underline text-sm text-gray-800 hover:text-gray-900">
				Forgot your password?
				</Link>
			</div>

			<jet-button class="mt-5 w-full py-3 custom-hover" :class="{ 'opacity-25': form.processing }" :disabled="form.processing">
				Log in
			</jet-button>

		</form>
	</jet-authentication-card>
</template>

<script>
import JetAuthenticationCard from "@/Jetstream/AuthenticationCard.vue";
import JetAuthenticationCardLogo from "@/Jetstream/AuthenticationCardLogo.vue";
import JetButton from "@/Jetstream/Button.vue";
import JetInput from "@/Jetstream/Input.vue";
import JetCheckbox from "@/Jetstream/Checkbox.vue";
import JetLabel from "@/Jetstream/Label.vue";
import JetValidationErrors from "@/Jetstream/ValidationErrors.vue";
import { Head, Link } from "@inertiajs/inertia-vue3";

export default {
	components: {
		Head,
		JetAuthenticationCard,
		JetAuthenticationCardLogo,
		JetButton,
		JetInput,
		JetCheckbox,
		JetLabel,
		JetValidationErrors,
		Link,
	},

	props: {
		canResetPassword: Boolean,
		status: String,
	},

	data() {
		return {
			form: this.$inertia.form({
				email: "",
				password: "",
				remember: false,
			}),
		};
	},

	methods: {
		submit() {
			this.form
				.transform((data) => ({
					...data,
					remember: this.form.remember ? "on" : "",
				}))
				.post(this.route("login"), {
					onFinish: () => this.form.reset("password"),
				});
		},
	},
};
</script>
